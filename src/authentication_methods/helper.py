"""Authentication-method related utilities"""

VIEW_OTHERS_AUTHENTICATION_METHODS_PERM = 'view-others-auth-methods'
"""The permission required in order to view other peoples authentication
methods.
"""

CAN_MODIFY_OTHERS_AUTHENTICATION_METHODS_PERM = 'modify-others-auth-methods'
"""The permission required in order to modify other peoples authentication
methods, such as through granting and revoking permissions."""

CAN_VIEW_OTHERS_AUTH_EDIT_NOTES_PERM = 'view-others-auth-edit-notes'
"""The permission required in order to view the username of other users which
have modified an authentication method and the note they made if there is one."""

CAN_VIEW_DELETED_AUTHENTICATION_METHODS_PERM = 'view-deleted-auth-methods'
"""The permission required to view deleted authentication methods"""
